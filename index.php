                <p class="u-text u-text-3">
                    A Buerotech é um projeto inovador com o tema Cidades Inteligentes e foco em "Monitoramento de bueiros na prevenção de alagamentos".
                    Desenvolvido por alunos do Curso de Desenvolvimento de Software Multiplataforma da Fatec Ogari de Castro Pacheco e com a orientação dos professores das materias de Internet das Coisas (IoT), Laboratório WEB e Integração e Entrega Continua nosso objetivo é levar a Cidade de Itapira cada vez mais perto de ser uma Cidade Inteligente.<br><br>

                    Nesse projeto trazemos um Sistema Integrado composto por cesto coletor e Sensor Volumétrico que monitora o bueiro em que está instalado e transmite os dados em tempo real via LORA para uma plataforma de gestão que emite um aviso quando o bueiro está próximo de atingir sua capacidade máxima. Com isso os responsáveis pela limpeza podem realizar a manutenção necessária com mais eficiencia, evitando o acumulo de lixo e liberando o bueiro para escoamento de águas pluviais.

                    <br><br>

                    Diminuindo assim, a incidencia de alagamentos em zonas urbanas, trazendo diversos beneficios para a comunidade como:

                    <br>

                    <b>Segurança Pública:</b> Evita riscos à vida e à segurança das pessoas que vivem nas áreas afetadas por alagamentos, reduzindo o potencial de afogamentos e acidentes relacionados.

                    <br>

                    <b>Proteção de Propriedades e Infraestrutura:</b> Minimiza danos materiais a residências, empresas e infraestrutura pública, como estradas, pontes e sistemas de esgoto, economizando recursos financeiros tanto para os residentes quanto para o governo.

                    <br>

                    <b>Saúde Pública:</b> Diminui o risco de contaminação da água potável e a propagação de doenças transmitidas pela água, protegendo a saúde dos moradores da comunidade.

                    <br>

                    <b>Preservação Ambiental:</b> Reduz o impacto negativo dos alagamentos nos ecossistemas locais, prevenindo danos à fauna e flora, bem como a contaminação de corpos d'água e áreas naturais.

                    <br>

                    <b>Melhoria da Mobilidade Urbana:</b> Evita interrupções no tráfego de veículos e no transporte público, garantindo uma mobilidade mais eficiente e segura para os moradores da região.

                    <br>

                    <b>Bem-Estar Psicológico:</b> Minimiza o estresse e a ansiedade causados pelos impactos emocionais e financeiros associados aos alagamentos, promovendo um maior bem-estar na comunidade.

                    <br>

                    <b>Desenvolvimento Econômico Sustentável:</b> Favorece um ambiente de negócios mais estável e atrativo, pois empresas e investidores são mais propensos a se estabelecer em áreas onde os riscos de alagamentos são gerenciados de forma eficaz.
